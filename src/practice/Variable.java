package practice;

public class Variable {
    public static void main(String[] args) {
        // 変数は値に名前をつけて扱いやすくするためのもの
        // 変数名 = 値 「代入」
        String name = "きの子";
        System.out.println("Hello " + name);

        // 型
        String number = "1" + "2";
        System.out.println(number);

        // 整数 int
        int numberInt = 1 + 2;
        System.out.println(numberInt);

        // boolean true(真) or false(偽) 2値しかない
        // 旗 (flag)
        boolean flag = true;
        boolean flag2 = false;
    }
}
